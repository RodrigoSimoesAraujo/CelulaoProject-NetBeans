    private void criarNovaOrdemServico() {
        try {
            OrdemServicoBean newOS = OrdemServicoService.createOrdemServicoByCliente(
                    mainCliente,
                    txtMarcaCelular.getText(),
                    txtModeloCelular.getText(),
                    txtPartesEntregues.getText());
            mainOrdemServico = newOS;
            prepFieldsOrdemServicoEncontrada();
        } catch (Exception ex) {
            ex.printStackTrace();
            Alert.showAlertOnField("Dados incorretos da OS. \n Exception:" + ex.getMessage());
        }
    }
    
