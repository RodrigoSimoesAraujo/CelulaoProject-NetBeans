    public static void run(Cliente cliente) {
        run(cliente,null);
    }
    
    public static void run(Cliente cliente, OrdemServicoBean selectedCodOrdemServico) {
        HomeOrdemServico dialog = new HomeOrdemServico(cliente, selectedCodOrdemServico);
        
        if(cliente!=null && selectedCodOrdemServico!=null){
            dialog.setTitle(
                "Ordem de Serviço: " + selectedCodOrdemServico.getCodOrdem() +
                        " - Cliente: " + cliente.getNomeReferencia() );            
        }
        else if(cliente!=null){
            dialog.setTitle(
                "Nova ordem de Serviço - Cliente: " 
                        + cliente.getNomeReferencia() );
        }
        
        final Toolkit toolkit = Toolkit.getDefaultToolkit().getDefaultToolkit();
        final Dimension screenSize = toolkit.getScreenSize();
        final int x = (screenSize.width - dialog.getWidth()) / 2;
        final int y = (screenSize.height - dialog.getHeight()) / 2;
        dialog.setLocation(x, y);
    
        dialog.setVisible(true);
        dialog.requestFocusInWindow();
}

